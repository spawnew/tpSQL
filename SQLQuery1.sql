/* EJERCICIO4
*/
SELECT TIPO,SUM(PRECIO_ORI)AS TOTAL_PRECIO,AVG(PRECIO_ACT)AS PROMEDIO_PRECIOACT
FROM LIBRO
WHERE EDICION <>1946 
GROUP BY TIPO
HAVING SUM(PRECIO_ORI) > 40
ORDER BY TIPO;
